    public void buscarCliente(String cedula) throws ClassNotFoundException, SQLException{
        Class.forName(driver);
        String nombre = "";
         String direccion = "";
          String telefono = "";
           String ciudad = "";
           
        Connection conn=(Connection) DriverManager.getConnection(ruta,user,password);
        try (Statement st = conn.createStatement()) {
            ResultSet rs = st.executeQuery("SELECT * FROM clientes WHERE cedula='"+cedula+"'");
            while (rs.next())
            {
               nombre = rs.getString(3);
               direccion = rs.getString(4);
               telefono = rs.getString(6);
               ciudad = rs.getString(7);
            }
            if(!nombre.equals("")){
                faNombre.setText(nombre);
                faDireccion.setText(direccion);
                faTelefono.setText(telefono);
                faCiudad.setText(ciudad);
                coComprobar.setEnabled(true);
                coAgregar.setEnabled(true);
                factDescripcion.setEditable(true);
               
                tempoCedula = cedula;
            }else{
                JOptionPane.showMessageDialog(null, "Error: Cliente inexistente");
            }
            rs.close();
            st.close();
            conn.close();
          
         }
         
    }
    public void buscarContrato(String cedula) throws ClassNotFoundException, SQLException{
        Class.forName(driver);
        String fechaFin = "";
        String kilometros = "";
        String dias = "";
        int facturado=0;
        Connection conn=(Connection) DriverManager.getConnection(ruta,user,password);
        try (Statement st = conn.createStatement()) {
            ResultSet rs = st.executeQuery("SELECT * FROM contratos WHERE cedula='"+cedula+"' AND token=0");
            while (rs.next())
            { 
                    fechaFin = rs.getString(5);
                    kilometros = rs.getString(8);
                    dias = rs.getString(9);
                    tempoPlaca = rs.getString(3);
                if(Integer.parseInt(rs.getString(10))==1){
                   facturado=1;
                }
               
            }
            
            if(!fechaFin.equals("")){
                if(facturado!=1){
                    Double toBeTruncated = Double.parseDouble(kilometros);
                    Double truncatedDouble = BigDecimal.valueOf(toBeTruncated)
                            .setScale(3, RoundingMode.HALF_UP)
                            .doubleValue();
                    coFechaFin.setText(fechaFin);
                    coKilo.setText(truncatedDouble.toString());
                    coDias.setText(dias);
                    buscarAuto();
                    total();
                    btnFact.setEnabled(true);
                }else{
                    JOptionPane.showMessageDialog(null, "Error: El contrato del cliente ya se encuentra facturado.");
                }
               
                
            }else{
                JOptionPane.showMessageDialog(null, "Error: El cliente no posee contratos activos");
            }
            rs.close();
            st.close();
            conn.close();
          
         }
         
    }
