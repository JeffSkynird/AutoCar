    public void total() throws ClassNotFoundException, SQLException{
         
        double costoFinal=Double.parseDouble(autoCosto.getText());
       
        int dias = Integer.parseInt(coDias.getText());
        double subtot =  BigDecimal.valueOf(costoFinal)
                .setScale(3, RoundingMode.HALF_UP)
                 .doubleValue()*dias;
        double iv = subtot*0.12;
        double totF = subtot+iv;
     
       
        subtotal.setText(String.valueOf(subtot));
        iva.setText(String.valueOf(BigDecimal.valueOf(iv)
                .setScale(3, RoundingMode.HALF_UP)
                 .doubleValue()));
        total.setText(String.valueOf(totF));
          
    }
